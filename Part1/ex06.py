# List of names
names = ['Jaganeesh', 'Aarjun', 'Ethan', 'BANANA', 'LEAF']

# ✅ Print the whole list
print(names)

# ✅ Print the first name
print(names[0])

# ✅ Print the third name
print(names[3])

# ✅ Print the last name
print(names[4])

# ✅ Print the middle two names using list slicing
print(names[2:4]) #Ethan, LEAF

